// while and do while

let i=0
while(i<=20)
   { console.log(i);
    i++;
   }

let a=["a","b","c"]
let j=0
while(j<a.length)
   { console.log(a[j]);
    j++ ;
   }
let score =1
do {
    console.log(score);
    score++  
}
while(score<=5);

// while loop will not execute if condition is false
// do while will execute once even if condition is false
